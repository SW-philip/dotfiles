def register_theme(name: str, palette: dict, source: str, force: bool = False) -> tuple[str, bool]:
    """Write all theme files and return (slug, already_existed).

    If the theme directory already exists with a palette.sh, that palette is
    used for regenerating derived files instead of the auto-derived one — this
    preserves hand-crafted palettes.  Pass force=True to overwrite everything.
    """
    slug = slugify(name)
    theme_dir = TEAMS_DIR / slug
    already = theme_dir.exists()
    theme_dir.mkdir(parents=True, exist_ok=True)

    existing_sh = theme_dir / f"palette-{slug}.sh"
    if already and existing_sh.exists() and not force:
        palette = _read_sh_palette(existing_sh)
        print(f"  ℹ️  Preserving existing palette for {slug} (use --force to regenerate)")
    else:
        write_nix(theme_dir / f"palette-{slug}.nix", palette, name)
        write_sh(theme_dir / f"palette-{slug}.sh", palette, name)

    write_nemo(theme_dir / "nemo.css", palette, name)
    write_ghostty(theme_dir / "ghostty.conf", palette, name)
    write_niri(theme_dir / "niri.toml", palette, name)
    write_fuzzel(theme_dir / "fuzzel.ini", palette, name)
    write_wleave(theme_dir / "wleave.css", palette, name)
    write_wvkbd(theme_dir / "wvkbd-colors.sh", palette, name)
    write_waybar_css(theme_dir / "waybar-style.css", palette, name)

    kate_colors, kate_syntax = write_kate(theme_dir / "kate", palette, name)
    print(f"  ✅ Generated Kate theme: {name}")



    # Wallpaper Generation
    if MAKE_WALLPAPER.exists():
        print(f"🖼️  Generating wallpaper for {slug}...")
        # cwd ensures we are in your config root when running bash
        result = subprocess.run(["bash", str(MAKE_WALLPAPER), slug], cwd=str(NIXOS_ROOT))
        if result.returncode != 0:
            print(f"⚠️  Wallpaper generation failed (exit {result.returncode})")

    return slug, already

def activate_theme(slug: str, theme_dir: Path) -> None:
    """Activate theme instantly."""
    import shutil
    import time

    palette_sh = theme_dir / f"palette-{slug}.sh"
    state_dir = Path.home() / ".local/state"
    state_dir.mkdir(parents=True, exist_ok=True)

    # ── Waybar palette + CSS ──────────────────────────────────────────────────
    waybar_palette = Path.home() / ".config/waybar/palette.sh"
    waybar_palette.unlink(missing_ok=True)
    shutil.copy2(palette_sh, waybar_palette)

    waybar_css_src = theme_dir / "waybar-style.css"
    waybar_css_dst = Path.home() / ".config/waybar/style.css"
    if waybar_css_src.exists():
        waybar_css_dst.unlink(missing_ok=True)
        shutil.copy2(waybar_css_src, waybar_css_dst)
        print("  → Waybar CSS applied")

    (state_dir / "theme").write_text(slug)

    # --- Kate Activation ---
    kate_colors_src = theme_dir / f"kate-{slugify(slug)}.colors"
    kate_syntax_src = theme_dir / f"kate-{slugify(slug)}-syntax.xml"

    # KDE Color Schemes location
    kate_colors_dst = Path.home() / ".local/share/color-schemes" / f"{slugify(slug)}.colors"
    kate_colors_dst.parent.mkdir(parents=True, exist_ok=True)

    # Kate Syntax location (may vary by version, usually katepart5 or katepart6)
    # We try the most common path first
    kate_syntax_dst = Path.home() / ".local/share/katepart5/syntax" / f"{slugify(slug)}.xml"
    kate_syntax_dst.parent.mkdir(parents=True, exist_ok=True)

    if kate_colors_src.exists():
        kate_colors_dst.unlink(missing_ok=True)
        shutil.copy2(kate_colors_src, kate_colors_dst)
        print(f"  ✅ Installed Kate Color Scheme")

    if kate_syntax_src.exists():
        kate_syntax_dst.unlink(missing_ok=True)
        shutil.copy2(kate_syntax_src, kate_syntax_dst)
        print(f"  ✅ Installed Kate Syntax Highlighting")

    # Restart Kate to apply changes
    subprocess.run(["pkill", "kate"], capture_output=True)
    time.sleep(0.5)

    # ── Wallpaper ─────────────────────────────────────────────────────────────
    wallpapers = sorted(theme_dir.glob("wallpaper-*.png"))
    if wallpapers:
        (state_dir / "wallpaper").write_text(str(wallpapers[0]))
        subprocess.run(["systemctl", "--user", "restart", "swaybg"], capture_output=True)

    # ── Fuzzel ────────────────────────────────────────────────────────────────
    fuzzel_src = theme_dir / "fuzzel.ini"
    fuzzel_dst = Path.home() / ".config/fuzzel/fuzzel.ini"
    if fuzzel_src.exists():
        fuzzel_dst.parent.mkdir(parents=True, exist_ok=True)
        fuzzel_dst.unlink(missing_ok=True)
        shutil.copy2(fuzzel_src, fuzzel_dst)
        print("  → Fuzzel theme applied")

    # ── Ghostty ───────────────────────────────────────────────────────────────
    ghostty_src = theme_dir / "ghostty.conf"
    ghostty_dst = Path.home() / ".config/ghostty/config"
    if ghostty_src.exists():
        ghostty_dst.parent.mkdir(parents=True, exist_ok=True)
        ghostty_dst.unlink(missing_ok=True)
        shutil.copy2(ghostty_src, ghostty_dst)
        subprocess.run(["pkill", "-SIGUSR2", "ghostty"], capture_output=True)
        print("  → Ghostty theme applied (new windows pick it up immediately)")

    # ── GTK3 (Nemo + Deluge) ─────────────────────────────────────────────────
    nemo_css_src = theme_dir / "nemo.css"
    gtk3_css_dst = Path.home() / ".config/gtk-3.0/gtk.css"
    if nemo_css_src.exists():
        gtk3_css_dst.parent.mkdir(parents=True, exist_ok=True)
        gtk3_css_dst.unlink(missing_ok=True)
        shutil.copy2(nemo_css_src, gtk3_css_dst)
        print("  → GTK3 theme applied (Nemo + Deluge; restart apps to pick up)")

    # ── wleave ────────────────────────────────────────────────────────────────
    wleave_css_src = theme_dir / "wleave.css"
    wleave_css_dst = Path.home() / ".config/wleave/style.css"
    if wleave_css_src.exists():
        wleave_css_dst.parent.mkdir(parents=True, exist_ok=True)
        wleave_css_dst.unlink(missing_ok=True)  # remove nix store symlink if present
        shutil.copy2(wleave_css_src, wleave_css_dst)
        print("  → wleave theme applied")

    # ── wvkbd ─────────────────────────────────────────────────────────────────
    wvkbd_src = theme_dir / "wvkbd-colors.sh"
    wvkbd_dst = Path.home() / ".config/wvkbd/colors.sh"
    if wvkbd_src.exists():
        wvkbd_dst.parent.mkdir(parents=True, exist_ok=True)
        wvkbd_dst.unlink(missing_ok=True)
        shutil.copy2(wvkbd_src, wvkbd_dst)
        active = subprocess.run(["systemctl", "--user", "is-active", "wvkbd"], capture_output=True)
        if active.returncode == 0:
            subprocess.run(["systemctl", "--user", "restart", "wvkbd"], capture_output=True)
        print("  → wvkbd theme applied")

    # ── Reload Waybar (CSS first, then full restart) ──────────────────────────
    subprocess.run(["pkill", "-SIGUSR1", "waybar"], capture_output=True)
    subprocess.run(["makoctl", "reload"], capture_output=True)
    time.sleep(0.3)
    subprocess.run(["pkill", "-f", "waybar-weather"], capture_output=True)
    subprocess.run(["pkill", "-SIGUSR2", "waybar"], capture_output=True)

    print(f"  ✅ Activated: {slug}")

