def _adjust(hex_col: str, l: float = 0, s: float = 0, h: float = 0) -> str:
    """Adjusts HSL values with clamping and returns a hex string."""
    hue, sat, light = _hex_to_hsl(hex_col)
    return _hsl_to_hex((hue + h) % 1.0, max(0, min(1, sat + s)), max(0, min(1, light + l)))

def fetch_api_palette(query: str, cache: dict) -> dict | None:
    cache_key = query.lower().strip()
    if cache_key in cache:
        print(f"⏭️  Using cached palette for '{query}'")
        return cache[cache_key]

    url = f"{COLOR_MAGIC_API}?q={query.replace(' ', '+')}"
    try:
        print(f"🔍 Fetching '{query}' from Color Magic API...")
        resp = requests.get(url, timeout=10)
        resp.raise_for_status()
        results = resp.json()

        if not results: return None

        best = max(results, key=lambda x: x.get('likesCount', 0))
        colors = best['colors']
        if len(colors) < 5: return None

        # 1. Map the 5 base colors
        initial = {
            "BASE": colors[0], "LOVE": colors[1], "ROSE": colors[2],
            "PINE": colors[3], "FOAM": colors[4],
        }

        # 2. Derive the full 51-key palette using the math engine
        full_palette = derive_full_palette(initial)

        # 3. Pack and Cache
        result = {
            "query": query,
            "palette_name": best['text'],
            "tags": best.get('tags', []),
            "palette": full_palette,
            "fetched_at": datetime.now().isoformat()
        }

        cache[cache_key] = result
        save_cache(cache)
        print(f"✅ Cached '{query}' -> {best['text']}")
        return result

    except Exception as e:
        print(f"❌ Error fetching API: {e}", file=sys.stderr)
        return None

def derive_full_palette(mapped: dict) -> dict:
    """Takes a dict with BASE, LOVE, ROSE, PINE, FOAM and derives the full depth."""
    mode = _theme_mode(mapped["BASE"])
    is_dark = mode == "dark"

    def _to_rgb(h_str):
        h = h_str.lstrip('#')
        return f"{int(h[0:2],16)},{int(h[2:4],16)},{int(h[4:6],16)}"

    # --- 1. STRUCTURAL DEPTH ---
    # Larger L gaps (9%/17% vs 5%/10%) and wider hue shift (9°/16° vs 3.6°/7.2°) so each
    # elevation is visually distinct even at low saturation.
    surface_l = 0.09 if is_dark else -0.08
    overlay_l = 0.17 if is_dark else -0.18

    mapped["SURFACE"] = _adjust(mapped["BASE"], l=surface_l, s=0.03, h=-0.025 if is_dark else 0.025)
    mapped["OVERLAY"] = _adjust(mapped["BASE"], l=overlay_l, s=0.06, h=-0.044 if is_dark else 0.044)
    mapped["OVERLAY_RGB"] = _to_rgb(mapped["OVERLAY"])

    # --- 2. HIGHLIGHTS ---
    h_step = 0.12 if is_dark else -0.12
    mapped["HIGHLIGHT_LOW"]  = _adjust(mapped["BASE"], l=h_step)
    mapped["HIGHLIGHT_MED"]  = _adjust(mapped["BASE"], l=h_step * 1.6, s=0.05)
    mapped["HIGHLIGHT_HIGH"] = _adjust(mapped["BASE"], l=h_step * 2.2, s=0.10)

    # --- 3. TEXT HIERARCHY ---
    mapped["TEXT"] = _get_text_color(mapped["BASE"])
    mapped["TEXT_PRIMARY"] = mapped["TEXT"]
    mapped["TEXT_SECONDARY"] = _adjust(mapped["TEXT"], l=-0.25 if is_dark else 0.25, s=-0.1)
    mapped["SUBTLE"] = _adjust(mapped["BASE"], l=0.3 if is_dark else -0.3, s=-0.05)
    mapped["MUTED"] = _adjust(mapped["BASE"], l=0.15 if is_dark else -0.15, s=-0.1)
    mapped["MUTED_ICON"] = _adjust(mapped["BASE"], l=0.20 if is_dark else -0.20)

    # --- 4. HARMONIC ACCENTS ---
    # Tint + small hue shift from the input team colors — no large hue jumps.
    # IRIS: cool-accent slot; nudge FOAM slightly toward violet.
    # GOLD: warm-amber slot; pick whichever of LOVE/ROSE/PINE is closest to
    #       orange (H≈0.08) and nudge it further toward amber.
    mapped["IRIS"] = _adjust(mapped["FOAM"], h=0.06, s=0.05)

    def _circ_dist(a, b):
        d = abs(a - b) % 1.0
        return min(d, 1.0 - d)

    warm_key = min(["LOVE", "ROSE", "PINE"],
                   key=lambda k: _circ_dist(_hex_to_hsl(mapped[k])[0], 0.08))
    wh, ws, wl = _hex_to_hsl(mapped[warm_key])
    # Nudge 25% toward orange (H=0.08) using the correct circular direction
    gold_diff = (0.08 - wh + 0.5) % 1.0 - 0.5
    mapped["GOLD"] = _hsl_to_hex(
        (wh + gold_diff * 0.25) % 1.0,
        max(0.0, min(1.0, ws - 0.05)),
        max(0.0, min(1.0, wl + (0.08 if is_dark else -0.08))),
    )

    # Ensure readability
    for acc in ["LOVE", "PINE", "FOAM", "IRIS", "GOLD", "ROSE"]:
        _, _, al = _hex_to_hsl(mapped[acc])
        if is_dark and al < 0.4: mapped[acc] = _adjust(mapped[acc], l=0.2, s=0.1)
        if not is_dark and al > 0.7: mapped[acc] = _adjust(mapped[acc], l=-0.2, s=0.1)

    # --- 5. UNIFIED HOVERS ---
    h_lift = 0.15 if is_dark else -0.15
    mapped["HOVER_BG"] = _adjust(mapped["BASE"], l=h_lift, s=0.05)
    for key in ["MUTED", "FOAM", "PINE", "GOLD", "LOVE", "ROSE", "IRIS"]:
        mapped[f"HOVER_{key}_BG"] = _adjust(mapped[key], l=h_lift, s=0.1)

    # Legacy hover aliases
    mapped["HOVER_TEAL_BG"]   = mapped["HOVER_FOAM_BG"]
    mapped["HOVER_GREEN_BG"]  = mapped["HOVER_PINE_BG"]
    mapped["HOVER_GOLD_BG"]   = mapped["HOVER_GOLD_BG"]
    mapped["HOVER_ORANGE_BG"] = _adjust(mapped["LOVE"], h=0.05, l=h_lift)

    # --- 6. GHOSTTY INVERSE ---
    if is_dark:
        mapped["GHOSTTY_BG"] = _adjust(mapped["GOLD"], l=0.4, s=-0.15)
        mapped["GHOSTTY_FG"] = _adjust(mapped["BASE"], l=-0.1)
    else:
        mapped["GHOSTTY_BG"] = _adjust(mapped["PINE"], l=-0.5, s=0.2)
        mapped["GHOSTTY_FG"] = _adjust(mapped["BASE"], l=0.4)

    # --- 7. WAYBAR & SHADOWS ---
    mapped["WB_BASE"] = _adjust(mapped["BASE"], l=surface_l * 0.5)
    mapped["WB_SURFACE"] = mapped["SURFACE"]
    mapped["WB_OVERLAY"] = mapped["OVERLAY"]

    for t in ["BASE", "SURFACE", "OVERLAY"]:
        target = mapped[t]
        mapped[f"GRAD_{t}_HI"] = _adjust(target, l=0.04, s=0.02)
        mapped[f"GRAD_{t}_LO"] = _adjust(target, l=-0.04, s=-0.02)

    mapped["SHADOW"] = _adjust(mapped["BASE"], l=-0.25 if is_dark else -0.4, s=0.15, h=-0.05)
    mapped["SHADOW_RGB"] = _to_rgb(mapped["SHADOW"])
    mapped["BORDER_ACCENT_RGB"] = _to_rgb(mapped["LOVE"])
    mapped["BORDER_IRIS_RGB"] = _to_rgb(mapped["IRIS"])
    mapped["INACTIVE_BORDER"] = mapped["MUTED"]
    mapped["BORDER_ACCENT"] = _adjust(mapped["LOVE"], l=-0.05)

    # --- 8. TINTS & STATES ---
    mapped["TINT_PINE_DARK"] = _adjust(mapped["PINE"], l=-0.2)
    mapped["TINT_PINE_MID"] = _adjust(mapped["PINE"], l=-0.1)
    mapped["TINT_CRITICAL_BG"] = _adjust(mapped["LOVE"], l=0.2 if is_dark else -0.2, s=-0.2)
    mapped["WARNING"] = _adjust(mapped["LOVE"], h=0.05)
    mapped["CRITICAL"] = mapped["LOVE"]
    mapped["CAUTION"] = mapped["GOLD"]

    # --- 9. ROLE MAPPINGS ---
    mapped.update({
        "BATTERY_CRIT": mapped["LOVE"], "BATTERY_LOW": mapped["GOLD"],
        "BATTERY_MED": mapped["ROSE"], "BATTERY_HIGH": mapped["FOAM"],
        "BATTERY_FULL": mapped["PINE"], "ACCENT_PRIMARY": mapped["IRIS"],
        "ACCENT_SECONDARY": mapped["FOAM"]
    })

    return mapped

