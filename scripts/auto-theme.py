def main():
    parser = argparse.ArgumentParser(description="Auto-generate and activate themes from teams or keywords.")
    parser.add_argument("query", nargs="?", help="Team name or keyword (e.g., 'lakers', 'neapolitan')")
    parser.add_argument("--rose-pine", action="store_true", help="Use official Rose Pine palette")
    parser.add_argument("--manual", metavar="HEX_LIST", help="Manual 5 colors: #base,#love,#rose,#pine,#foam")
    parser.add_argument("--list", action="store_true", help="List available teams")
    parser.add_argument("--list-api", metavar="KEYWORD", help="Preview API results for a keyword")
    parser.add_argument("--register-only", action="store_true", help="Generate files but don't activate")
    parser.add_argument("--force", action="store_true", help="Overwrite existing hand-crafted palette files")
    args = parser.parse_args()

    # 1. PRE-FLIGHT DATA LOADING
    teams = []
    if TEAM_DATA_FILE.exists():
        teams = json.loads(TEAM_DATA_FILE.read_text())
    api_cache = load_cache()

    # 2. GLOBAL HELP CHECK
    # Only show help if NO query is provided AND no special flags are used
    if not args.query and not (args.rose_pine or args.manual or args.list or args.list_api):
        parser.print_help()
        sys.exit(1)

    # 3. FLAG: --list
    if args.list:
        if not teams:
            print("No team data found. Run enrich-teamcolors.py first.")
            return
        col_w = max(len(t["name"]) for t in teams) + 2
        for t in sorted(teams, key=lambda x: x["name"]):
            slug = slugify(t["name"])
            registered = (TEAMS_DIR / slug).exists()
            mark = "✓" if registered else " "
            print(f"  {mark} [{t['league']:10s}]  {t['name']:{col_w}}  {slug}")
        return

    # 4. FLAG: --rose-pine
    if args.rose_pine:
        print("🌹 Using Official Rose Pine Palette...")
        if args.query and "dawn" in args.query.lower():
            name, initial = "Rose Pine Dawn", {
                "BASE": "#faf4ed", "LOVE": "#b4637a", "ROSE": "#d7827e",
                "PINE": "#286983", "FOAM": "#56949f"
            }
        else:
            name, initial = "Rose Pine Official", {
                "BASE": "#191724", "LOVE": "#eb6f92", "ROSE": "#ebbcba",
                "PINE": "#31748f", "FOAM": "#9ccfd8"
            }

        p = derive_full_palette(initial)
        slug, _ = register_theme(name, p, "manual", force=args.force)
        if not args.register_only:
            activate_theme(slug, TEAMS_DIR / slug)
        return

    # 5. FLAG: --manual
    if args.manual:
        codes = args.manual.split(',')
        if len(codes) != 5:
            print("❌ Error: --manual requires exactly 5 hex codes (base,love,rose,pine,foam)")
            return
        initial = {"BASE": codes[0], "LOVE": codes[1], "ROSE": codes[2], "PINE": codes[3], "FOAM": codes[4]}
        p = derive_full_palette(initial)
        slug, _ = register_theme("Manual Theme", p, "manual", force=args.force)
        if not args.register_only:
            activate_theme(slug, TEAMS_DIR / slug)
        return

    # 6. FLAG: --list-api
    if args.list_api:
        res = fetch_api_palette(args.list_api, api_cache)
        if res:
            p = res["palette"]
            print(f"Query: {res['query']} -> {res['palette_name']}")
            print(f"Colors: {', '.join([p['BASE'], p['LOVE'], p['IRIS'], p['GOLD']])}")
        else:
            print("No results found.")
        return

    # 7. SEARCH EXECUTION (Teams first, then API)
    # At this point, we know args.query exists because of the check in step 2.

    # Try Team Match
    team_match = None
    query_slug = slugify(args.query)
    for t in teams:
        team_slugs = [slugify(t["name"])] + [slugify(a) for a in t.get("aliases", [])]
        if query_slug in team_slugs:
            team_match = t
            break

    if team_match:
        print(f"🏆 Found Team: {team_match['name']} ({team_match['league']})")
        p = derive_full_palette(team_match["palette"])
        slug, _ = register_theme(team_match["name"], p, "team", force=args.force)
        if not args.register_only:
            activate_theme(slug, TEAMS_DIR / slug)
        return

    # Try API Match
    print(f"🔍 No team found. Trying API for '{args.query}'...")
    api_result = fetch_api_palette(args.query, api_cache)

    if not api_result:
        print(f"❌ No theme found for '{args.query}'.")
        sys.exit(1)

    p = api_result["palette"]
    theme_name = f"{api_result['query']} - {api_result['palette_name']}"
    slug, _ = register_theme(theme_name, p, "api", force=args.force)

    print(f"  ✅ Generated: {slug}")
    if not args.register_only:
        activate_theme(slug, TEAMS_DIR / slug)

if __name__ == "__main__":
    main()
