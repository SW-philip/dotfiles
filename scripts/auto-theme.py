def write_sh(path: Path, p: dict, name: str):
    """Writes a full shell-compatible palette for Waybar and wallpaper scripts."""
    lines = [
        "#!/usr/bin/env bash",
        f"# {name} — generated palette",
        "",
        "# --- All Theme Variables ---"
    ]

    # Export every key found in the palette dictionary
    for key, value in p.items():
        lines.append(f'export {key}="{value}"')

    # Ensure explicit wallpaper mapping is also present
    lines.extend([
        "",
        "# --- Wallpaper Mapping ---",
        f'export BG_DARK="{p.get("BASE", "#1a1a1a")}"',
        f'export BG_LIGHT="{p.get("SURFACE", "#2a2a2a")}"',
        f'export ICE_SHADOW="{p.get("BASE", "#1a1a1a")}"',
        f'export ICE_MID="{p.get("LOVE", p.get("ROSE", "#ff00ff"))}"',
        f'export ICE_HIGHLIGHT="{p.get("FOAM", p.get("IRIS", "#ffffff"))}"',
        f'export CONE_SHADOW="{p.get("GOLD", "#965a28")}"',
        f'export CONE_MID="{p.get("ROSE", "#d28e46")}"',
        'export STICKER_COLOR="#ffffff"'
    ])

    path.write_text("\n".join(lines) + "\n")
    path.chmod(0o755)

